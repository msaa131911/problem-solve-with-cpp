/*দেওয়া থাকবে:
| Variable | Meaning                     |
| -------- | --------------------------- |
| `n`      | কাজ করতে হবে এমন বার সংখ্যা |
| `a`      | দৈর্ঘ্য                     |
| `b`      | প্রস্থ                    
প্রতি বর্গমিটারে 2 লিটার রঙ লাগে।|*/

#include<iostream>
using namespace std;
int main()
{
    int n,a,b;
    cin>>n>>a>>b;
    cout<<n*a*b*2;//formula -> n×a×b×2
}
